    private void sacuvatiBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sacuvatiBTNActionPerformed
        // Update user data

//        Get user input but no check included
        String idBroj = idBrojTF.getText();
        String ime = imeTF.getText();
        String brojTelefona = brojTelefonaTF.getText();
        String starost = starostTF.getText();
        String pol = polTF.getText();
        String krvnaGrupa = krvnaGrupaTF.getText();
        String adresa = adresaTF.getText();

//        connect and query db
        try (Connection dbConnection = ConnectionDB.getConn()) {
            // create statement
            Statement st = dbConnection.createStatement();
//            execute statement with query
            st.executeUpdate("UPDATE pacijentbaza SET name ='" + ime + "',contactNumber='" + brojTelefona + "',age='" + starost + "',gender='" + pol + "',bloodGroup='" + krvnaGrupa + "',address='" + adresa + "' WHERE idpacijentbaza='" + idBroj + "'");

            JOptionPane.showMessageDialog(null, "Successfully Updated");

            clearInput();

        } catch (Exception e) {
            System.err.println("Problem: " + e.getMessage());
            JOptionPane.showMessageDialog(null, "Check your input.");
        }

    }//GEN-LAST:event_sacuvatiBTNActionPerformed

