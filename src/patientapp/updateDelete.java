    private void traziBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_traziBTNActionPerformed
        // Get user id

//       Get patient id
        String patientId = idBrojTF.getText();

        try (Connection dbConnection = ConnectionDB.getConn()) {

            Statement st = dbConnection.createStatement();

            String query = "SELECT * FROM pacijentbaza WHERE idpacijentbaza = " + patientId;

//            Get patient from DB
            ResultSet rs = st.executeQuery(query);

            if (rs.next()) {

                imeTF.setText(rs.getString(2));
                brojTelefonaTF.setText(rs.getString(3));
                starostTF.setText(rs.getString(4));
                polTF.setText(rs.getString(5));
                krvnaGrupaTF.setText(rs.getString(6));
                adresaTF.setText(rs.getString(7));

                noIdNumberLBL.setVisible(false);

            } else {
                noIdNumberLBL.setVisible(true);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Connection error in: " + getClass());
        }

    }//GEN-LAST:event_traziBTNActionPerformed

